#import "Calculator.h"

@implementation Calculator


+(float) add : (float)  x : (float)y {
    return x+y;
}


+(float) sub : (float)  x : (float)y {
    return x-y;
    
}


+(float) mult: (float)  x : (float)y {
    return  x*y;
    
}


+(float) div : (float)  x : (float)y {
    return x/y;
}

@end
